class Solution {
  public:
    int equalSum(int n, vector<int> &a) {
        // code here
        if(n == 1)
            return 1;
        int rightSum=accumulate(a.begin()+1, a.end(), 0);
        int leftSum=0;
        for(int i=1; i<a.size(); i++)
        {
            rightSum -= a[i];
            leftSum += a[i-1];
            // cout<<rightSum<<" "<<leftSum<<endl;
            if(leftSum == rightSum)
                return i+1;
        }
        return -1;
    }
};


